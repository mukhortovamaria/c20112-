Users = {
    'Марія': '20-30',
    'Олександр': '30-40',
    'Вікторія': '40-50',
    'Анастасія': '20-30',
    'Дмитро': '50+'}
name = input("Введіть ім'я користувача: ")
if name in Users:
    print(f"{name} належить до вікової групи {Users[name]}.")
else:
    print(f"Користувача з ім'ям {name} не знайдено.")
